function add (num1, num2){
  return num1 + num2 ;
}

function multifly (num1, num2){
  return num1 * num2 ;
}

function substrack (num1, num2){
  return num1 - num2 ;
}

function devide (num1, num2){
  return num1 / num2 ;
}

function modulo (num1, num2){
  return num1 % num2 ;
}

function calculator(num1, num2, operator){
  return operator(num1, num2);
}
